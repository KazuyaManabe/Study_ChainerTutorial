# スカラとは、一つの値または変数
# x が実数のスカラであることをしばしば「x∈R」と書きます。 R は実数のスカラからなる集合を表し、「A∈B」は「A は B に属する」という意味です。
# 従って、x∈R で「x は実数のスカラからなる集合に属する」、すなわち「x は実数のスカラである」と解釈できます。

# ベクトル (vector) は、スカラを 1 方向に並べたものです。
# 列ベクトルと、行ベクトル。
print([])  # 列ベクトル

# ベクトルに含まれるスカラの数のことを、そのベクトルの次元と呼びます。
# ベクトル x がN次元のベクトルで、ベクトルを構成するスカラが実数であることを、数式を用いて「x∈RN」とも書きます。

print([[1, 2], [7, 4], [5, 6]])
